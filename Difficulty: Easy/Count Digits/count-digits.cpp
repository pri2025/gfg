class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int cnt = 0;
        int num = N;
        while(num>0){
            int d = num%10;
            num/=10;
            if(d != 0 && N%d == 0)
            cnt++;
        }
        return cnt;
    }
};

